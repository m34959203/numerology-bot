"""Каталог услуг (сид) и форматирование цен.

Цены в тенге (₸) — основная валюта. Доллар показывается справочно по курсу
USD_RATE_KZT. price_stars не задаётся: оплата через Telegram Stars отключена на
время тестирования (см. settings.payment_imitation), сейчас оплата имитируется.
"""

# Справочный курс для отображения цены в долларах (₸ за 1 $).
USD_RATE_KZT = 525


def price_usd(price_tenge: int) -> int:
    """Цена в долларах (округление до целого) по справочному курсу."""
    return round(price_tenge / USD_RATE_KZT)


def format_price(price_tenge: int) -> str:
    """Человекочитаемая цена: «3 000 ₸ (~$6)»."""
    tenge = f"{price_tenge:,}".replace(",", " ")
    return f"{tenge} ₸ (~${price_usd(price_tenge)})"


